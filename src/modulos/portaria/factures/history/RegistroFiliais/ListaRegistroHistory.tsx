import { useEffect, useState, type ChangeEvent } from "react";
import Template from "./registroFiliaisCss"
import SearchIcon from "@mui/icons-material/Search";
import Api from "../../../service/apiRegistro/apiRegistro"
import portariaApi from "../../../service/api"

import VisibilityIcon from '@mui/icons-material/Visibility';
import ImageIcon from '@mui/icons-material/Image';
import DeleteIcon from '@mui/icons-material/Delete';
// Versão mais recente do MUI
import DownloadIcon from '@mui/icons-material/Download';
import { ModalGlobalComponent } from "../../../../../components/modalGlobal/modalGlobalComponent";
import { Paginator } from "../../../../../components/paginator/paginator";
import { useNavigate } from "react-router-dom";
const listaSelect = [
  { nome: "Aberto", value: true },
  { nome: "Fechado", value: false }
]
import SelectVariants from "../../../../../components/select/selectFiltro";
import { PopupComponent } from "../../../../../components/popup/popupComponent";
import { subjet } from "../../../../../jwt/jwtservice";
import { Avatar, Box, CircularProgress, IconButton, Paper, Stack, TextField, Tooltip, Typography } from "@mui/material";
import apiUsuario from "../../../../PaginaInicial/service/apiUsuario";
export const ListaRegistroComponent = () => {
  const [lista, setLista] = useState<any[]>([])
  const [ativo, setAtivo] = useState(false);
  const [id, setId] = useState("");
  const navigate = useNavigate()
  const [busca, setBusca] = useState("")
  const [msg, setMsg] = useState("")
  const [ativoBtn, setBtnAtivo] = useState(true)
  const user = subjet()
  const [exibeImagem, setExibeImagem] = useState(false);
  const [loading, setLoading] = useState(false);
  const [selectedValue, setSelectedValue] = useState<any | null>(null);
  const [selectedFilial, setSelectedFilial] = useState<number | null>(user?.filial);

  const [totaPage, setTotalPage] = useState(0)

  const [item, setItem] = useState<any>()

  const onSubmit = async (pageUnique?: any) => {
    setLoading(true)
    const resposta = await Api.findAll(selectedFilial as any, busca, selectedValue as any, pageUnique);
    if (resposta) {
      setTimeout(() => {
        setLoading(false);
        setLista(resposta.content);
        setTotalPage(resposta?.totalPages);
      }, 1000);

    }
  }
  useEffect(() => {
    if (busca.trim() === "") {
      onSubmit(); // se o campo estiver vazio, busca toda a lista
    }
  }, [busca])
  const [filiais, setFiliais] = useState<any[]>([]);
  const handleSearchFiliais = async () => {
    // const resposta = await apiFiliais.lista();
    // if (resposta?.filial) {
    //   setFiliais(resposta?.filial)
    // }

    const resposta = await apiUsuario.FiliaisUsuario(user?.id);
    if (resposta?.acess) {
      setFiliais(resposta.acess);
    }

  }

  useEffect(() => {
    handleSearchFiliais()
  }, [])
  const hendleDelete = (item: any,) => {
    setMsg(`Deseja realmente deletar o item ${item.id}`)
    setAtivo(true);
    setId(item.id)
  }
  const handleDeleteHistory = async () => {
    await portariaApi.deletarPortaria(id, user?.id);
    setMsg(`${id} Deletado com sucesso`);
    setBtnAtivo(false)
    await onSubmit();
    setAtivo(false);

  }

  const handleModalImg = (item: any) => {
    setExibeImagem(true)
    setItem(item)
  }
  const handleNextPage = (_: ChangeEvent<unknown>, value: number) => {
    const valueBusca = value - 1;
    onSubmit(valueBusca);
  }
  const handleExibeImagem = async (imagem: any, nomeImagem: any) => {
    const response = await fetch(imagem)
    const blob = await response.blob();
    const url = window.URL.createObjectURL(blob);

    const link = document.createElement("a");
    link.href = url;
    link.download = nomeImagem;
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);

    window.URL.revokeObjectURL(url);
  }
  const retornaCorStatus = (status: any) => {
    switch (status) {
      case "AGUARDANDO_ENTRADA": return "#3b82f6"; // Azul vibrante
      case "AGUARDANDO_SAIDA": return "#eab308";   // Amarelo âmbar
      case "SAIDA_LIBERADA": return "#22c55e";     // Verde esmeralda
      default: return "#ef4444";                    // Vermelho
    }
  }
  const handleVisualItem = (id: any) => {
    setLoading(true)
    setTimeout(() => {
      navigate(`/portaria/controle/detalhes-registro?order=${id}`, { replace: false, state: { refresh: Date.now() } })
    }, 2000)
  }

  // ... sua lógica permanece idêntica
  return (
    <Template.container>
      {/* Cabeçalho com MUI Stack */}
      <Stack direction="row" justifyContent="space-between" alignItems="center" sx={{ mb: 3 }}>
        <Typography variant="h4" sx={{ fontWeight: 700, color: '#1e293b' }}>
          Histórico Portaria
        </Typography>
      </Stack>

      <Paper elevation={0} sx={{ p: 2, borderRadius: '16px', border: '1px solid #e2e8f0' }}>
        <Template.FormSub>
          {/* Barra de Busca e Filtros Otimizada */}
          <Box sx={{
            display: 'flex',
            gap: 2,
            alignItems: 'center',
            flexWrap: 'wrap',
            bgcolor: '#f8fafc',
            p: 2,
            borderRadius: '12px'
          }}>
            <TextField
              variant="outlined"
              size="small"
              placeholder="Pesquisar por nome ou placa..."
              value={busca}
              onChange={(e) => setBusca(e.target.value)}
              sx={{ bgcolor: 'white', minWidth: 250 }}
            />

            <SelectVariants value={selectedValue} onChange={setSelectedValue} titulo={"Status"} list={listaSelect} />
            <SelectVariants value={selectedFilial} onChange={setSelectedFilial} titulo={"Filial"} list={filiais} />

            <Tooltip title="Pesquisar">
              <IconButton
                onClick={() => onSubmit()}
                sx={{ bgcolor: '#6366f1', color: 'white', '&:hover': { bgcolor: '#4f46e5' } }}
              >
                <SearchIcon />
              </IconButton>
            </Tooltip>

            <Box sx={{ flexGrow: 1 }} />
            <Paginator totalPage={totaPage} handleNextPage={handleNextPage} />
          </Box>

          <Template.TableContainer>
            <Template.Table>
              <thead>
                <tr>
                  <th>Visitante</th>
                  <th>Protocolo</th>
                  <th>Tipo / Placa</th>
                  <th>Ocupação Liberada / Tipo de Acesso</th>
                  <th>Local</th>
                  <th>Status</th>
                  <th>Entrada</th>
                  <th>Saída</th>
                  <th>Fiscal Entrada</th>
                  <th>Fiscal Saida</th>
                  <th style={{ textAlign: 'right' }}>Ações</th>
                </tr>
              </thead>
              <tbody>
                {loading ? (
                  <Template.loadingRow>
                    <td colSpan={11}>
                      <Template.loadingContainer>
                        <CircularProgress size={28} />
                        <Typography variant="body2">
                          Carregando registros...
                        </Typography>
                      </Template.loadingContainer>
                    </td>
                  </Template.loadingRow>
                ) : lista.length === 0 ? 
                  (
                    <tr>
                      <td colSpan={10}>
                        <Template.semItens>
                          <Template.iconSemItens />
                          <Typography variant="h6" color="textSecondary">Nenhum registro encontrado</Typography>
                          <Typography variant="body2" color="textSecondary">Tente ajustar seus filtros de busca.</Typography>
                        </Template.semItens>
                      </td>
                    </tr>
                  ) : (
                  lista.map((item, key) => (
                    <tr key={key}>
                      <td>
                        <Stack direction="row" spacing={1.5} alignItems="center">
                          <Avatar
                            sx={{ width: 40, height: 40, border: '2px solid #e2e8f0' }}
                            src={item?.visitante?.imagem || "/static/images/avatar/2.jpg"}
                          />
                          <Box>
                            <Typography variant="body2" sx={{ fontWeight: 600 }}>{item?.nomeCompleto || "---"}</Typography>
                            <Typography variant="caption" color="textSecondary">{item?.filialSocitado}</Typography>
                          </Box>
                        </Stack>
                      </td>
                      <td><Typography variant="caption" sx={{ fontFamily: 'monospace', fontWeight: 700 }}>#{item?.protocolo || 'PENDENTE'}</Typography></td>
                      <td>
                        <Typography variant="body2">{item?.visitante?.tipoPessoa || "Visitante"}</Typography>
                        <Typography variant="caption" sx={{ bgcolor: '#f1f5f9', px: 0.5, borderRadius: 1 }}>{item?.placaVeiculo || "Sem Veículo"}</Typography>
                      </td>
                      <td>
                        <Typography variant="body2">{item?.ocupacaoLiberada || "Visitante"}</Typography>
                        <Typography variant="caption" sx={{ bgcolor: '#f1f5f9', px: 0.5, borderRadius: 1 }}>{item?.visitante?.recorrencia?.nome || "Sem Recorrencia"}</Typography>
                      </td>
                      <td>{item?.bloco}</td>
                      <td><Template.Chip color={retornaCorStatus(item?.status)}>{item?.status.replace("_", " ")}</Template.Chip></td>
                      <td><Typography variant="caption">{item?.entrada?.dataEntrada ? new Date(item.entrada.dataEntrada).toLocaleString() : "---"}</Typography></td>
                      <td><Typography variant="caption">{item?.saida?.dataSaida ? new Date(item.saida.dataSaida).toLocaleString() : "---"}</Typography></td>
                      <td><Typography variant="caption" sx={{ fontWeight: 500 }}>{item?.entrada?.nomeFiscal || "---"}</Typography></td>
                      <td><Typography variant="caption" sx={{ fontWeight: 500 }}>{item?.saida?.nomeFiscal || "---"}</Typography></td>

                      <td>
                        <Template.trBTN>
                          <Tooltip title="Visualizar Detalhes">
                            <IconButton size="small" onClick={() => handleVisualItem(item.id)}><VisibilityIcon fontSize="small" /></IconButton>
                          </Tooltip>
                          {item?.entrada?.imagem && (
                            <Tooltip title="Ver Fotos">
                              <IconButton size="small" onClick={() => handleModalImg(item)} color="primary"><ImageIcon fontSize="small" /></IconButton>
                            </Tooltip>
                          )}
                          <Tooltip title="Excluir">
                            <IconButton size="small" onClick={() => hendleDelete(item)} color="error"><DeleteIcon fontSize="small" /></IconButton>
                          </Tooltip>
                        </Template.trBTN>
                      </td>
                    </tr>
                  ))
                )}
              </tbody>
            </Template.Table>
          </Template.TableContainer>
        </Template.FormSub>
      </Paper>

      {/* Modal de Imagem Refatorado com MUI */}
      {exibeImagem && (
        <ModalGlobalComponent cancelar={() => setExibeImagem(false)}>
          <Box sx={{ p: 1 }}>
            <Stack direction="row" spacing={2} alignItems="center" sx={{ mb: 3 }}>
              <Avatar src={item?.visitante?.imagem} sx={{ width: 60, height: 60 }} />
              <Typography variant="h6">Evidências: {item?.visitante.nomeCompleto}</Typography>
            </Stack>

            <Template.imagemArea>
              {['entrada', 'saida'].map((tipo) => item?.[tipo]?.imagem && (
                <Template.divArea key={tipo}>
                  <Stack direction="row" justifyContent="space-between" alignItems="center" sx={{ mb: 1 }}>
                    <Typography variant="subtitle2" sx={{ textTransform: 'capitalize' }}>{tipo}</Typography>
                    <IconButton size="small" onClick={() => handleExibeImagem(item?.[tipo]?.imagem, `Foto_${tipo}`)}>
                      <DownloadIcon fontSize="small" color="primary" />
                    </IconButton>
                  </Stack>
                  <Template.imgem src={item?.[tipo]?.imagem} alt={`Imagem ${tipo}`} />
                </Template.divArea>
              ))}
            </Template.imagemArea>
          </Box>
        </ModalGlobalComponent>
      )}

      {ativo && <PopupComponent handleCancel={() => setAtivo(false)} handleConfirm={handleDeleteHistory} message={msg} ativoBtn={ativoBtn} />}
    </Template.container>
  );
}